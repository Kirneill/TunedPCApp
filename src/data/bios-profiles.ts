/** Get profiles matching the detected CPU name. Returns empty if no match. */
export function getProfilesForCpu(cpuName: string | null | undefined): BiosProfile[] {
  if (!cpuName) return [];
  const upper = cpuName.toUpperCase();
  return BIOS_PROFILES.filter(p =>
    p.targetCpus.some(cpu => upper.includes(cpu.toUpperCase()))
  );
}
